// Não podemos criar variáveis com palavras reservadas
// Variáveis precisam ter nomes significativos
let nomeCliente = 'João'
console.log(nomeCliente)

// Não podemos começar o nome da variável com um número
// De preferência começar com letras minúsculas 
// Apenas funções construtoras começar com letra maiúsculas(let 1nome = nome)
// Variáveis não podem conter espaços ou traços (nome-Cliente) 
// Utilizamos camelCase (começar com letra minuscula e finalizar com maiúscula)
let nomeCompletoDoCliente = 'Luiz Otávio'
console.log(nomeCompletoDoCliente)

// Variáveis são case-sensitive (LETRAS MINUSCULAS E MAISCULAS FAZEM MUITA DIFERENÇA)
let nomeCliente1 = 'Luiz'
nomeCliente1 = 'Marcos'
console.log(nomeCliente1)
